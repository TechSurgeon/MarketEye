public class ParseUtil {

	public static final String[] months= { "Jan", "Feb", "Mar", "Apr",
        "May", "Jun", "Jul", "Aug", "Sep",
        "Oct", "Nov", "Dec" };
	
	public static int monthStringToInt(String smonth) { // assumed to be of format "APR"

		if (smonth.equals(months[0])) {
		return 1;
		} else if (smonth.equals(months[1])) {
		return 2;
		} else if (smonth.equals(months[2])) {
		return 3;
		} else if (smonth.equals(months[3])) {
		return 4;
		} else if (smonth.equals(months[4])) {
		return 5;
		} else if (smonth.equals(months[5])) {
		return 6;
		} else if (smonth.equals(months[6])) {
		return 7;
		} else if (smonth.equals(months[7])) {
		return 8;
		} else if (smonth.equals(months[8])) {
		return 9;
		} else if (smonth.equals(months[9])) {
		return 10;
		} else if (smonth.equals(months[10])) {
		return 11;
		} else if (smonth.equals(months[11])) {
		return 12;
		} else
		return 0;
		}
}
