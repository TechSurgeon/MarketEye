package com.sa.marketslayer.library.databeans;

import java.util.Date;

public class MBMessage {

	//private String messageID;
	public String id;// unique key
	public String m;
	public String user;
	public Date d;
	
	public MBMessage(){
		
	}

	public MBMessage(String local_messageID, String minimessage, String userID, Date date) {
		//super();
		this.id = local_messageID;
		this.m = minimessage;
		this.user = userID;
		this.d = date;
	}
	
	public String toString(){
		return ""+id+" "+m+" "+user+" "+d.toString();
	}
}
